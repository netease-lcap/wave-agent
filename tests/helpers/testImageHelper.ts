import { writeFileSync } from "fs";
import { join } from "path";
import { tmpdir } from "os";

/**
 * 创建临时测试图片
 * @returns 临时图片文件的路径
 */
export const createTestImage = (): string => {
  const tempPath = join(tmpdir(), `test-clipboard-${Date.now()}.png`);
  // 创建一个最小的PNG文件（1x1像素，白色）
  const pngData = Buffer.from([
    0x89,
    0x50,
    0x4e,
    0x47,
    0x0d,
    0x0a,
    0x1a,
    0x0a, // PNG signature
    0x00,
    0x00,
    0x00,
    0x0d, // IHDR chunk length
    0x49,
    0x48,
    0x44,
    0x52, // IHDR
    0x00,
    0x00,
    0x00,
    0x01, // width = 1
    0x00,
    0x00,
    0x00,
    0x01, // height = 1
    0x08,
    0x02,
    0x00,
    0x00,
    0x00, // bit depth = 8, color type = 2 (RGB), compression = 0, filter = 0, interlace = 0
    0x90,
    0x77,
    0x53,
    0xde, // CRC
    0x00,
    0x00,
    0x00,
    0x0c, // IDAT chunk length
    0x49,
    0x44,
    0x41,
    0x54, // IDAT
    0x08,
    0x99,
    0x01,
    0x01,
    0x00,
    0x00,
    0x00,
    0xff,
    0xff,
    0x00,
    0x00,
    0x00,
    0x02,
    0x00,
    0x01, // compressed data
    0xe2,
    0x21,
    0xbc,
    0x33, // CRC
    0x00,
    0x00,
    0x00,
    0x00, // IEND chunk length
    0x49,
    0x45,
    0x4e,
    0x44, // IEND
    0xae,
    0x42,
    0x60,
    0x82, // CRC
  ]);
  writeFileSync(tempPath, pngData);
  return tempPath;
};
